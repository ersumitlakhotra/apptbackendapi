import pool from "../config/db.js";

const createUserTable = async () => {

    const queryText=`
CREATE TABLE IF NOT EXISTS public."user"
(
    id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 9223372036854775807 CACHE 1 ),
    cid bigint,
    username text COLLATE pg_catalog."default",
    password text COLLATE pg_catalog."default",
    role text COLLATE pg_catalog."default",
    permission text COLLATE pg_catalog."default",
    status text COLLATE pg_catalog."default",
    profilepic bytea[],
    createdat timestamp with time zone,
    modifiedat timestamp with time zone,
    email text COLLATE pg_catalog."default",
    cell text COLLATE pg_catalog."default",
    rating text COLLATE pg_catalog."default",
    fullname text COLLATE pg_catalog."default",
    accounttype text COLLATE pg_catalog."default",
    CONSTRAINT user_pkey PRIMARY KEY (id),
    CONSTRAINT username UNIQUE (username),
    CONSTRAINT cid FOREIGN KEY (cid)
        REFERENCES public.company (id) MATCH FULL
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public."user"
    OWNER to postgres;`;

    try {
        pool.query(queryText);
        console.log("User Table Created");
    } catch (error) {
       
        console.log("Error Creating User table :",error); 
    }
}

export default createUserTable;
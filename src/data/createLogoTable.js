import pool from "../config/db.js";

const createLogoTable = async () => {

    const queryText = `
CREATE TABLE IF NOT EXISTS public.logo
(
    id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 9223372036854775807 CACHE 1 ),
    cid bigint,
    logo bytea,
    modifiedat timestamp with time zone,
    CONSTRAINT logo_pkey PRIMARY KEY (id),
    CONSTRAINT cid FOREIGN KEY (cid)
        REFERENCES public.company (id) MATCH SIMPLE
        ON UPDATE NO ACTION
        ON DELETE NO ACTION
)

TABLESPACE pg_default;

ALTER TABLE IF EXISTS public.logo
    OWNER to postgres;`;

    try {
        pool.query(queryText);
        console.log("Logo Table Created");
    } catch (error) {

        console.log("Error Creating Company table :", error);
    }
}

export default createLogoTable;